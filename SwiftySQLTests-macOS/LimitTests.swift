import XCTest

@testable import SwiftySQL

class LimitTests: XCTestCase {
    
    override func setUp() {
        super.setUp()
        
        student = Student()
        teature = Teature()
        lecture = Lecture()
        attending = Attending()
    }
    
    override func tearDown() {
        super.tearDown()
        
        student = nil
        teature = nil
        lecture = nil
        attending = nil
    }
    
    func testLimit() {
        XCTAssertSQL(SQL.Limit(limit: 1, offset: nil), "1")
        XCTAssertSQL(SQL.Limit(limit: 1, offset: 100), "1, 100")
    }
    
}
